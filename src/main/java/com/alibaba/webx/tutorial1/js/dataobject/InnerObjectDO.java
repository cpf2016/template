package com.alibaba.webx.tutorial1.js.dataobject;

public class InnerObjectDO {
	
	private String[] arrayMethods;
	
	private String[] dateMethods;
	
	private String[] stringMethods;

	public String[] getArrayMethods() {
		return arrayMethods;
	}

	public void setArrayMethods(String[] arrayMethods) {
		this.arrayMethods = arrayMethods;
	}

	public String[] getDateMethods() {
		return dateMethods;
	}

	public void setDateMethods(String[] dateMethods) {
		this.dateMethods = dateMethods;
	}

	public String[] getStringMethods() {
		return stringMethods;
	}

	public void setStringMethods(String[] stringMethods) {
		this.stringMethods = stringMethods;
	}
	
}
