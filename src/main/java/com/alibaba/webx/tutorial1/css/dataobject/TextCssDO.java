package com.alibaba.webx.tutorial1.css.dataobject;

public class TextCssDO {
	private String selector;
	
	private String lineHeight;
	
	private String lineHeightUnit;
	
	private String textIndent;
	
	private String textIndentUnit;
	
	private String textAlign;
	
	public String getSelector() {
		return selector;
	}

	public void setSelector(String selector) {
		this.selector = selector;
	}

	public String getLineHeight() {
		return lineHeight;
	}

	public void setLineHeight(String lineHeight) {
		this.lineHeight = lineHeight;
	}

	public String getLineHeightUnit() {
		return lineHeightUnit;
	}

	public void setLineHeightUnit(String lineHeightUnit) {
		this.lineHeightUnit = lineHeightUnit;
	}

	public String getTextIndent() {
		return textIndent;
	}

	public void setTextIndent(String textIndent) {
		this.textIndent = textIndent;
	}

	public String getTextIndentUnit() {
		return textIndentUnit;
	}

	public void setTextIndentUnit(String textIndentUnit) {
		this.textIndentUnit = textIndentUnit;
	}

	public String getTextAlign() {
		return textAlign;
	}

	public void setTextAlign(String textAlign) {
		this.textAlign = textAlign;
	}
}
