package com.alibaba.webx.tutorial1.common.utils.dataobject;

public class DateDO {
	
	private String type;
	
	private String createType;
	
	private String[] methods;
	
	private String dateUnit;
	
	private String operateType;
	
	private String number;
	
	private String dateBetween;
	
	private String dateCompare;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String[] getMethods() {
		return methods;
	}

	public void setMethods(String[] methods) {
		this.methods = methods;
	}

	public String getDateUnit() {
		return dateUnit;
	}

	public void setDateUnit(String dateUnit) {
		this.dateUnit = dateUnit;
	}

	public String getOperateType() {
		return operateType;
	}

	public void setOperateType(String operateType) {
		this.operateType = operateType;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getDateBetween() {
		return dateBetween;
	}

	public void setDateBetween(String dateBetween) {
		this.dateBetween = dateBetween;
	}

	public String getDateCompare() {
		return dateCompare;
	}

	public void setDateCompare(String dateCompare) {
		this.dateCompare = dateCompare;
	}

	public String getCreateType() {
		return createType;
	}

	public void setCreateType(String createType) {
		this.createType = createType;
	}
	
	
	
}
